## 四、功能与用途
### 4.1 实用功能
- **御寒保暖**：高领设计有效阻挡风寒，配合夹里、絮棉的袄可适应北方冬季低温。
- **活动便利**：两侧开衩结构增加腿部活动空间，琵琶袖收口设计便于劳作与日常行动。
- **防护洁净**：护领可拆卸换洗，延长主衣使用寿命；高领减少灰尘沾染。

### 4.2 社会功能
- **身份标识**：通过面料、纹样、纽扣材质区分等级。贵族使用织金妆花缎、宝石纽扣；民间多用棉布、素绸。命妇礼服搭配霞帔、凤冠构成完整礼服体系。
- **场合适配**：短袄配马面裙为日常常服；长袄/长衫可作为盛常服；大衫霞帔则属命妇礼服层级。明代皇后常服体系中，大衫、霞帔与鞠衣共同构成燕居冠服，用于各类礼仪场合。
