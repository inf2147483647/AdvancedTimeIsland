## 一、简介
竖领长衫/长袄是明代中后期女性常服的核心上衣形制，属于明制袄裙体系。**单层无衬里为衫，双层夹里为袄**，衣长过膝至小腿部位的称为长衫/长袄，是明代女性服饰从交领向立领演变的典型代表，兼具实用性与礼仪性，上至宫廷后妃、下至民间士庶均有穿着。

## 二、历史沿革
1. **起源**：竖领（又称立领）形制出现于明代中期，由传统交领、直领逐步演变而来，最早可追溯至明景泰、天顺年间的宫廷画像，明英宗孝肃周皇后容像中已出现竖领内衣形态。
2. **盛行**：明嘉靖、万历年间走向鼎盛，从贵族阶层向民间普及，北方地区逐渐以竖领取代直领成为主流，南方则保持直领与竖领并行的格局。
3. **影响**：明代竖领是后世中式立领服饰的直接源头，与清代旗装立领、民国旗袍立领存在形制演变关联，但其结构、领型比例与后世立领有明显区别。
4. **文物佐证**：孔府旧藏、嘉兴王店明墓、湖州邵南夫妇合葬墓、定陵等均有竖领袄衫实物出土，时间跨度覆盖明中期至明末。

