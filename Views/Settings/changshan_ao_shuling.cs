## 一、简介
竖领长衫与袄是明代中后期汉族女性最具代表性的上衣形制，属明制汉服核心品类。**单层无衬里为”衫“**，适合春夏穿着；**双层夹里为“袄”**，适合秋冬御寒。按衣长可分为短袄（衣长至腰胯）与长袄/长衫（衣长至小腿），按襟型分为竖领大襟（右衽斜襟）与竖领对襟两类。领口配以金银子母扣是其标志性特征，整体风格端庄雅致，兼具实用性与装饰性，上至皇后命妇、下至平民女子皆可穿着。

## 二、历史沿革
### 2.1 起源与形成
- **明初**：女性上衣以交领右衽为主，承袭宋元旧制。洪武元年《衣冠复古诏》推行恢复汉唐衣冠，但实际融合了大量宋元服饰元素。
- **明中期（正统—正德年间）**：竖领雏形出现，由交领护领演化而来。景泰年间已有竖领内衣图像记载，天顺年间皇后画像中可见明确的竖领内搭。北京南苑苇子坑正德十年（1515年）夏儒夫妇墓出土的女衫已呈现立领对襟形制，是目前已知最早的竖领对襟女衫实物。
- **嘉靖年间**：竖领形制正式确立并逐渐流行，被明人称为“竖领“，又称“扣身袄儿”，是明代服饰的重要创新。

### 2.2 盛行与发展
- **明后期（万历—崇祯）**：竖领袄衫全面普及，成为女性常服主流。定陵出土的万历朝皇后服饰中已无交领款式，全部为立领形制。山东孔府旧藏、各地明墓出土文物均印证了其广泛流行。
- **气候动因**：明代中后期正值“小冰河期”，气温显著下降，高领设计有效提升了颈部保暖性，是竖领流行的重要客观因素。
- **文化背景**：明代理学兴盛，对女性身体遮蔽要求提高，竖领贴合颈部的设计也符合当时的社会审美与礼教规范。
- **社会风尚**：晚明竞奢风气盛行，女性通过金银纽扣、精致面料彰显身份与品味，推动了竖领袄衫的装饰化发展。

### 2.3 后世传承
清初“剃发易服”推行“男从女不从”政策，汉族女性竖领袄衫得以短暂保留。至清中期逐渐发生变异，领高降低并加饰缘边，最终演变为后世中式立领的雏形，但与明制竖领在结构上已有本质区别。

## 三、形制结构
### 3.1 领部结构
- **竖领（立领）**：方角瘦高型，由交领直接发展而来，区别于清代弧形立领。领高约5—8厘米，可直立也可向下翻折穿着，灵活适应不同气温。
- **纽扣配置**：领口通常缀1—2枚金属子母扣，对襟款式门襟可缀多达7枚纽扣。纽扣以金银打造，常镶嵌宝石，造型多为花卉、吉祥纹样等，兼具固定与装饰功能。
- **护领传统**：早期竖领多缝有白色护领，可拆卸换洗以保持领口清洁，这一设计源自交领护领传统。

### 3.2 襟型分类
| 襟型 | 结构特点 | 流行程度 |
|------|----------|----------|
| 竖领大襟（斜襟） | 右衽直线斜襟至腋下系带，延续汉服右衽传统 | 更为普遍，正式场合多用 |
| 竖领对襟 | 两襟对称，门襟以纽扣固定，穿脱便捷 | 晚明盛行，日常居家常用 |

### 3.3 袖型与衣身
- **袖型**：以琵琶袖为主，袖身宽大、袖口收窄，呈小口大袖形态；亦有大袖、窄袖等变体。袖宽通常40—55厘米，袖口宽14—20厘米。
- **衣长**：短袄衣长约60—75厘米，至腰胯位置；长袄/长衫衣长约110—140厘米，长及小腿中部。
- **开裾**：两侧多开衩至腋下附近，便于行走与活动，同时丰富着装层次感。
- **比例特征**：据定陵出土女袄实测数据，衣身与袖部比例遵循传统几何美学。以孝端显皇后竖领女夹衣为例，身长79厘米，通袖长240厘米，袖阔57.2厘米，整体比例协调，体现明代服饰的形制规范。

