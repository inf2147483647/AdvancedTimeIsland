4. **第四层：外搭（可选）**
   - 比甲：无袖对襟长背心，秋冬增加保暖
   - 披风：直领对襟大袖外披，正式场合使用

### 5.2 下装搭配
- **标准搭配**：[马面裙](route:AdvancedTimeIslandMamianQunCeZhe)，系于袄衫之外，裙门居中对齐
- **晚明流行**：百褶裙，搭配短袄更显灵动
- **裙门**需与上衣门襟中线对齐，保持整体垂直线条

### 5.3 穿戴步骤
1. 先穿着主腰与衬裤，再穿上中衣
2. 套上竖领袄衫，大襟款先将右襟叠于左襟之下，系好腋下系带
3. 由上至下依次扣好领口及门襟纽扣，确保领部端正不歪斜
4. 系好马面裙，调整裙门至身体正前方
5. 按需外搭比甲或披风，搭配相应头饰与鞋履

## 六、注意事项
### 6.1 尺寸选择
- **胸围放量**：净胸围基础上加放20—25厘米，保证活动余量与垂坠效果
- **通袖长度**：竖领衫通袖长约230厘米为宜，过短会显得局促
- **衣长比例**：长袄衣长至小腿中部最得体，短袄以盖住裙腰3—5厘米为佳
- **领围贴合**：竖领不宜过紧，以能插入一指为宜，避免卡颈不适

### 6.2 穿着规范
- **右衽原则**：大襟款必须右衽（左襟压右襟），不可左衽
- **纽扣对齐**：门襟纽扣需沿中线垂直排列，不可错位歪斜
- **领部端正**：竖领需立起服帖，左右对称，避免一侧翻折一侧直立
- **裙摆平整**：马面裙褶子需整理顺直，裙门居中，避免扭转

### 6.3 面料与保养
- **面料选择**：夏季宜用纱、罗、苎麻等透气面料；秋冬选用缎、绸、棉布等厚实面料
- **洗涤护理**：丝绸面料建议干洗，棉布可轻柔手洗，金属纽扣需避免沾水生锈
- **收纳存放**：悬挂存放避免褶皱，竖领部位可垫衬布防变形，梅雨季节注意防潮防虫

### 6.4 常见误区
- 明制竖领为方角直领，不同于清代旗袍的弧形立领，结构上有本质区别
- 竖领是汉服固有元素，并非由旗装传入，明代早于清代立国即已成熟
- 袄与衫的核心区别在于是否夹里，而非长度，短款也可称为袄

