## 注意事项

襦裙是中国古代服饰中一种上下裙装，但不可将所有上下裙装都称为襦裙。清朝的旗服褂裙也不是襦裙。

襦裙和曲裾分属于汉服的两个不同类别。曲裾有左襟续任后绕，裙在衣内，衣襟至少过膝，不可作短襦看待（曲裾有相对的长短，不可等同于""深衣""这个概念，但是的确有""曲裾深衣""这个款式）。襦裙的下裙无类似曲裾的绕襟式样。

衫裙约流行于魏晋时期，和襦的主要区别：魏晋南北朝时襦多指加絮短衣，而衫无絮，郭璞注曰：""今或呼衫为单襦""。";

