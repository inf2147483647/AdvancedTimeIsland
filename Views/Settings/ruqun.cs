## 在宋代

### 一、整体造型与审美风格

#### 1. 腰线回落，廓形修长

襦裙腰线从唐代的齐胸、高腰回落至腰部，以齐腰襦裙为绝对主流；整体廓形从唐代的宽博丰腴逐渐转向瘦长修身，裙长多拖地掩足，姿态更显端庄内敛。

#### 2. 气质素雅克制

受程朱理学""存天理、灭人欲""的观念影响，服饰摒弃大面积奢华装饰与浓艳配色，追求淡雅恬静的视觉效果，审美偏向内敛、温润。

### 二、上襦的形制特征

#### 1. 衣身短窄，束腰穿着

襦为短款上衣，衣长一般仅及腰间，穿着时多将下摆束入裙内，裙外再另系腰带，是典型的""上衣下裳""两截式结构。

#### 2. 领袖设计实用为主

领型以交领、直领对襟最为常见；袖型日常多为窄袖，兼顾劳作与行动便利，礼仪场合也有宽袖礼服款；腰身、袖口相对唐代更为宽松舒适。

#### 3. 品类随季节分化

按厚度可分为单襦（薄款，近于衫，春夏穿着）和复襦（夹棉衬里，称夹袄、棉袄，秋冬保暖）；衣身的领口、袖口边缘多做刺绣或拼接缘饰，作为低调点缀。

### 三、下裙的形制特征

#### 1. 褶裙为绝对主流

宋代裙装以多褶为标志性特色，时兴""千褶""""百迭""裙（即百迭裙），裙褶排列整齐紧密，是后世百褶裙的雏形；此外还有前后开衩的旋裙、两片式的资裙、鱼尾状的褶裥裙等特色款式。

#### 2. 廓形随时代演变

北宋初期仍沿袭晚唐五代遗风，裙身较肥阔；北宋中期至南宋，裙型逐渐收窄，变得瘦长窄俏，垂坠感更强。

#### 3. 色彩与阶层区分

裙装色彩通常比上襦更鲜亮，名贵品类有郁金香根染制的黄裙、经典的石榴红裙等；老年妇女与民间劳动女性多穿深色素裙，风格质朴实用。

#### 4. 专属配饰玉环绶

裙腰的飘带上常悬挂一枚玉制圆环，称为""玉环绶""，作用是压住裙幅，避免行走时裙摆随风散开失仪，兼具装饰性与礼仪功能，是宋代襦裙的标志性细节。

