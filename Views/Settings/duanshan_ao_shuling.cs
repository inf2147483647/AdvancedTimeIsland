## 一、简介
明代女性服饰遵循""两截穿衣""的基本框架，上着衣、下着裙，整体风格端庄内敛。**短衫**与**袄**是明代女性最核心的上衣品类，二者核心区别在于薄厚单复：单层为衫，适合春夏穿着；双层夹里为袄，适合秋冬御寒。**竖领**（又称立领）是明代中后期发展出的标志性领型，可应用于衫、袄等多种上衣，是明代服饰最具辨识度的特征之一。

三者共同构成了明代女性日常常服的主体，上至宫廷后妃、命妇，下至平民女子皆可穿着，仅在面料、纹样、装饰上体现身份等级差异。

## 二、历史演变

### 2.1 短衫与袄的发展脉络
- **明初（洪武—天顺年间）**：女衫以交领右衽、大袖衫为主，衣长及腰，袖子多为宽身收口的""鱼肚袖""样式。这一时期服饰制度严格，形制简约朴素，等级差异主要体现在面料与纹样上，平民多穿素色棉麻布料。
- **明中期（正德—嘉靖年间）**：女衫形制开始发生显著变化，衣身逐渐放宽加长，立领对襟款式出现并逐步流行。北京南苑苇子坑夏儒夫妇墓（正德十年，1515年）出土的女衫，是目前已知较早的立领对襟衫实物。嘉靖时期女衫仍以交领为主，但衣长普遍延伸至膝下，袖宽也明显增加。
- **明晚期（隆庆—崇祯年间）**：竖领完全成熟定型，成为女性便装的主流款式，琵琶袖广泛应用，衣身版型更贴合身形，袖型则由宽大为先逐渐向窄直过渡。金属纽扣从实用构件演变为重要装饰元素，领型与衣襟的设计更加丰富多样。

袄的形制继承自宋元传统，明代进一步规范化。早期袄多为交领右衽制式，明中后期竖领袄成为主流，按长度分为短袄（不过膝）和长袄（过膝）两类，女性日常穿着以短袄为主。

### 2.2 竖领的起源与发展
竖领是明代本土发展出的原创领型，由传统交领逐步演化而来：
- **雏形阶段**：明中期交领领部逐渐加高，护领上缀金属扣固定，可完整包裹颈部，形成竖领的早期形态。《宪宗元宵行乐图》中宫中女子的交领已呈现高围脖颈、以扣具固定的特征。
- **出现时间**：从出土实物来看，竖领形制不晚于弘治十五年（1502年），南昌宁靖王夫人吴氏墓中已出现竖领大襟短袖衫；画像证据可追溯至景泰年间。
- **普及阶段**：正德年间竖领对襟和竖领大襟的文物数量增多，嘉靖后自上而下由宫廷向民间扩散，广泛流行。晚明时期高耸贴合颈部、以金属子母扣固定的竖领成为最具代表性的女性装束。
- **气候与文化动因**：明代中后期进入""小冰河期""，气温显著下降，竖领能够严密护住颈部、防止风寒入侵，是其普及的重要现实原因；同时它也契合了当时的社会审美与身体观念，成为服饰功能与文化需求结合的典型。

## 三、形制结构

### 3.1 短衫
- **定义**：单层无衬里的短款上衣，衣长通常在60—70厘米左右，覆盖腰至胯部位置。
- **领型**：主要有交领右衽、竖领大襟、竖领对襟三类，不同时期流行款式各有侧重。
- **袖型**：
  - 琵琶袖：腋部较窄，袖底呈自然弧形，大袖小口，形似琵琶。分小琵琶袖（袖宽约35cm，袖口13—17cm）和大琵琶袖（袖宽约53cm）两类，是明中后期最具代表性的袖型。
  - 窄袖/直袖：袖底平直，袖宽25—30cm，多作内穿或日常便装使用。
- **衣身结构**：采用传统""十字平面结构""，以直线裁剪为主，无现代服饰的分割线与省道；后中破缝，下摆微宽，两侧开裾；右侧腰间缝有系带用于固定衣襟。
- **典型实物**：孔子博物馆藏绿地织金纱翔凤纹短衫，衣长67.5厘米，交领右衽，琵琶袖，左右开裾，属明代吉服范畴。

### 3.2 袄
- **定义**：有衬里的上衣，双层夹里或夹棉填充，主要用于御寒。按长度分为短袄（不过膝盖）和长袄（过膝），女子日常居家、出行以短袄为主。
- **领型体系**：明代袄子领型丰富，包含直领大襟、直领对襟、圆领大襟、圆领对襟、竖领大襟、竖领对襟、方领对襟七类，其中竖领类是明代最具时代辨识度的款式。
- **结构特征**：长袖通裁，两侧开衩（称""开裾""）；交领类以右衽系带固定，竖领类领口缀纽扣、衣襟系带并用；衣身腋下宽松，下摆自然微展，符合传统平面裁剪的特征。
- **典型实物**：江西南昌宁靖王夫人吴氏墓出土有多件明代短袄实物，其中织金璎珞纹云肩妆花缎夹袄以织金缎为面料、绢质里衬，交领右衽琵琶袖制式，衣身两侧开裾，衣襟与腋下配有多组系带，是明中期贵族女袄的典型代表；同墓出土的织金凤纹云肩通袖襕夹袄衣长60厘米，通袖长211厘米，袖口宽16.5厘米，两侧开衩高15.2厘米，外襟与腋下各设3条系带系合，工艺考究。

### 3.3 竖领（立领）
- **结构本质**：竖领由传统交领演化而来，其雏形是交领上的护领与纽扣的结合，裁剪逻辑与交领一脉相承，核心差异在于领部高度与闭合方式。
- **形态特征**：领身直立贴合颈部，两领角呈方形，领高可根据穿着需求调节，也可翻折成小翻领样式穿着；领缘通常配有白色护领，可拆卸换洗，保持领口洁净。
- **分类**：
  - **竖领大襟**：右衽斜襟结构，继承了传统交领的衣襟形制，仅领部直立；领口通常设1—2粒金属扣固定，衣襟其余部分依靠系带系合，是明代中后期主流的常服款式。
  - **竖领对襟**：正中两襟对开通上下，用纽扣、搭袢或绳带系连，穿脱便捷；对襟两侧常配有装饰缘边，兼具实用性与装饰性，常作为外衣或便服穿着。
- **纽扣特征**：多使用金银子母扣、镶嵌宝石的纽扣，既是闭合构件也是重要的装饰元素；领口纽扣数量从1枚到数枚不等，晚明时期纽扣的装饰性愈发突出，成为女性服饰的点睛之笔。

## 四、功能与穿着场合

### 4.1 实用功能
- **季节适配**：衫为单层，轻薄透气，适合春夏穿着；袄为夹里/夹棉，保暖性强，适合秋冬御寒，二者共同构成了明代女性上衣的四季穿着体系。
- **保暖防护**：竖领能够严密包裹颈部，抵御风寒侵袭，适应明代中后期寒冷的气候特点，是服饰功能适应自然环境的典型体现。
- **日常劳作**：窄袖短衫行动便利，适合劳动与日常出行；琵琶袖空间充裕，可容纳手部，兼具美观与实用价值。

### 4.2 穿着场合
- **日常便服**：短衫、短袄是明代女性最普遍的日常着装，适用于居家、出行、会客等绝大多数生活场景。
- **吉庆场合**：采用织金、妆花等华丽面料的短衫袄属于吉服，用于时令节庆、寿宴筵席等吉庆场合。
- **礼服搭配**：竖领袄可作为内搭，外搭披风、比甲，与霞帔、马面裙、凤冠组合构成命妇礼服体系，彰显身份等级。
- **层次穿搭**：可单穿，也可作为内搭外罩比甲、披风，适应不同温度与场合的穿搭需求。

## 五、穿着方法

### 5.1 基础穿着顺序
1. **先着下装**：穿着中衣、衬裙，再穿马面裙或其他下裙；袄裙制式为上衣覆于裙外，因此先穿下裙再穿上衣。
2. **套入上衣**：将短衫/袄展开，双臂伸入袖中，整理衣身使前后对齐。
3. **固定衣襟**：
   - 交领款：先将左襟（里襟）叠于右侧，系好内侧系带；再将右襟（外襟）叠于左侧，依次系好腋下与腰间系带，确保右衽（衣襟向右掩）。
   - 竖领对襟款：先扣好领口纽扣，再自上而下依次扣好衣襟纽扣或系好系带。
   - 竖领大襟款：先扣好领部金属扣，再按交领方式系好腋下与腰间系带。
4. **整理定型**：整理领型使其贴合颈部，抚平衣身褶皱，调整袖子位置，确保下摆平整覆盖裙腰。

### 5.2 搭配层次
- **基础款**：短衫/短袄 + 马面裙，为明代女性最经典的日常搭配。
- **春秋叠穿**：短衫 + 比甲 + 马面裙，增加层次感与保暖性。
- **冬季叠穿**：短袄 + 长袄/披风 + 马面裙，多层穿搭抵御严寒。
- **领型调节**：竖领可完全竖起贴合颈部，也可翻折成小翻领穿着，修饰脖颈线条。

## 六、注意事项与保养

### 6.1 穿着注意事项
- **形制规范**：交领款式须保持右衽（衣襟向右掩），这是汉服的核心特征，不可左衽。
- **尺寸适配**：传统明制短衫袄通袖长较长，通常在180—230厘米左右，选购时需注意通袖长、衣长等关键尺寸，避免不合身。
- **穿着误区**：避免盲目追求复古感而忽视实际穿着体验；竖领不宜过紧，应保证颈部活动舒适。
- **场合适配**：素面简约款适合日常，织金妆花等华丽款适合节庆活动，日常穿着避免过度堆砌装饰。

### 6.2 洗涤与保养
- **洗涤方式**：
  - 普通棉麻面料：使用中性洗涤剂，冷水或温水手洗，轻柔揉搓，避免大力拧绞。
  - 织金、妆花、刺绣面料：建议局部清洁，整体洗涤需手洗并格外轻柔，保护纹样完整性。
  - 禁止使用漂白剂，避免面料褪色与纤维损伤。
- **晾晒方法**：阴干或平铺晾干，避免阳光直射暴晒，防止面料褪色、金线氧化；悬挂晾晒时注意支撑肩部，避免变形。
- **熨烫整理**：使用蒸汽熨烫，温度根据面料调整；织金、刺绣面需垫布熨烫，避免直接高温接触。
- **收纳保存**：存放于阴凉干燥处，避免潮湿霉变；贵重款式建议套防尘袋悬挂存放，定期通风检查。
- **破损修复**：大面积污渍、面料撕裂或刺绣损坏时，建议寻求专业汉服修复机构处理，勿自行操作以免造成二次损伤。

