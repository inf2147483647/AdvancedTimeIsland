## 一、简介

明代女性交领长衫与长袄是明制汉服「上衣下裳」体系中的核心日常服饰品类。二者结构同源，核心区别在于薄厚单复：单层无衬里为**衫**，有衬里或纳棉为**袄**；按长度划分，衣长过膝者称**长袄/长衫**，不及膝者为短袄/短衫。其标志性特征为**交领右衽**，通常与[马面裙](route:AdvancedTimeIslandMamianQunCeZhe)搭配组成「袄裙」，是明代女性从平民到命妇皆广泛穿着的服饰形制，兼具实用性与礼仪性。

## 二、历史沿革
### 2.1 形制渊源
交领是华夏服饰最古老的领式之一，但交领大襟短上衣并非汉族女装原生制式。辽代以前汉族女装多以长衣为主，宋代流行直领对襟衣；辽金元时期受北方游牧民族服饰影响，交领短上衣逐渐普及，明代在此基础上继承并系统化发展，形成了成熟的交领袄衫体系。

### 2.2 明代演变脉络
- **明初**：交领女袄领幅较宽，沿袭前代高交领样式，衣襟为直线斜襟，交叉点偏高，保暖性强，领口普遍加白色护领以防污损。
- **明中期（正德—嘉靖）**：衣身逐渐加长，袖幅放宽。嘉靖年间交领衫袄衣长普遍达75—90厘米，延伸至膝下；袖型以琵琶袖为主，袖宽显著增加，是交领袄裙的鼎盛阶段。
- **明中后期**：立领对襟款式开始兴起，但交领大襟仍是民间日常与中低层女性的主流选择，且工艺更趋精细，织金、妆花等装饰工艺广泛应用。现有研究结合50余座明代墓葬出土实物统计表明，明代中后期女袄主流领襟结构整体呈现出由传统交领大襟，逐步向竖领、圆领、方领等对襟形制转变的特征，这一变化与当时社会风气、织造技术进步及保暖功能需求直接相关。

