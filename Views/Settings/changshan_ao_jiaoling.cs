4. **搭配兼容**：可单独外穿，也可内搭于比甲、半臂、披风之下，构建丰富的穿搭层次。

## 五、穿着方法

1. **内搭打底**：先着主腰与中衣（交领白衫），确保领口整洁，避免走光与面料贴身。
2. **穿著衣身**：左臂先伸入袖管，再穿右臂，将衣身向后拉平，使后背中缝垂直居中。
3. **系合系带**：
   - 先系里襟领端系带，与左腋下内侧系带打结固定；
   - 再将左襟覆盖右襟，对齐交领交叉点，依次系合外襟与右腋下的3对系带；
   - 全程遵循**左衽压右衽**（右衽）原则，严禁右襟压左襟（左衽为丧制）。
4. **整理塑形**：整理领口使其贴合颈部，捋平衣身褶皱，对齐前后中缝，下摆自然垂坠。
5. **下装搭配**：裙门对齐身体正中，腰头位于自然腰线处，马面裙穿着完成后，再系束长袄。

## 六、注意事项

### 6.1 穿着礼仪与安全

- **右衽禁忌**：交领必须左襟压右襟（右衽），这是汉服最核心的礼仪规则，反向穿着为丧俗，需格外注意。
- **行动防护**：衣长过膝，上下楼梯、行走不平路面时，应轻提前侧裙摆3—5厘米，防止踩踏绊倒；避免勾挂尖锐物体。
- **坐姿规范**：落座时需整理后摆，将裙摆顺势收拢于身侧，避免压皱与坐脏面料。

### 6.2 洗护与保养

- **洗涤方式**：丝绸、织金、妆花面料必须**手洗**，水温不超过30℃，使用中性丝毛洗涤剂；禁止机洗、大力揉搓与拧干。
- **晾晒收纳**：阴干避光，直射阳光会导致褪色与丝纤维脆化；收纳前确保完全干燥，用宽肩带棉垫衣架悬挂，或折叠后夹入无酸纸，放置于干燥通风处，可放硅胶干燥剂防潮。
- **织金养护**：含金线的服饰避免摩擦，不可熨烫金线部位；长期存放需定期通风检查，防止金线氧化发黑(铜锌合金？)。

### 6.3 版型甄别要点

- 交领斜度自然，领缘无直角转折；
- 两侧必有开衩，无开衩为后世改良款；
- 琵琶袖袖型弧线流畅，袖口收窄比例协调；
- 后背中缝笔直，衣身放量宽松，非紧身剪裁。

