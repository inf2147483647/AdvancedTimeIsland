### 3.2 衫与袄的区分

- **衫**：单层，多为纱、罗、绸等轻薄面料，春夏季穿着
- **袄**：有衬里（夹袄）或纳棉絮（棉袄），秋冬季御寒使用
- 二者领型、袖型、剪裁结构完全一致，仅厚度与面料有别

## 四、功能与用途

1. **日常起居**：是明代女性最普遍的居家与出行常服，平民女性多着素面棉麻材质，仕宦家庭则用丝绸暗花面料。
2. **保暖御寒**：夹袄、棉袄为秋冬季核心保暖层，可内搭主腰、中衣，外罩比甲、披风，适应不同气温。
