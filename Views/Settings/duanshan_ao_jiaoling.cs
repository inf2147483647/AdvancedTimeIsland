## 五、穿着方法

1. **基础穿法（交领右衽）**

    1. 先穿好下裙（如马面裙），再穿上衫/袄，将后背衣领向下轻拉，使领口贴合颈部。

    2. 将右襟贴于身前，左襟覆盖于右襟之上，调整领口露出1~2厘米内领边。

    3. 先系内侧里襟系带，固定于左腋下，再依次系合外襟与右腋下的2~3根系带，松紧以贴身不紧绷为宜。

    4. 整理两侧开衩与下摆，使衣身平整，袖型自然垂顺。

2. **叠穿方法**

    - 春秋：内穿交领短衫，外搭交领短袄或比甲。

    - 冬季：短衫+夹袄+披袄/披风，由内到外衣长逐层递增，露出层次。


## 六、注意事项

### （一）穿着与搭配

- 遵循右衽原则，左襟压右襟为汉服正统穿法，右衽反向通常为丧制，日常穿着需注意区分。

- 搭配马面裙时，短衫袄下摆应置于裙外，这是明代袄裙制的典型特征，区别于前代襦裙的""襦在裙内""穿法。

- 宽袖款式用餐、劳作时可将袖口挽起，避免沾染污渍；行走时注意两侧开衩处，防止勾挂。


### （二）洗护与保养

- **洗涤**：真丝、织金、刺绣款必须手洗，使用冷水与中性洗涤剂，轻柔按压去污，禁止拧绞、机洗；棉麻款可装洗衣袋轻柔机洗。

- **晾晒**：反面阴干，避免阳光直射导致褪色、纤维老化；丝绸款平铺晾干，防止悬挂拉长变形。

- **收纳**：熨烫低温垫布进行，折叠存放于干燥通风处，贵重面料可放防虫剂，长期不穿建议用防尘罩悬挂。

- **护领维护**：可拆卸护领单独清洗更换，可有效延长主衣使用寿命。


