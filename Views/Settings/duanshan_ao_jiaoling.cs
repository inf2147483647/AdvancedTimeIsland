## 一、简介

交领短衫与交领袄是明代女性日常服饰体系中的核心上衣形制，同属""上衣下裳""制中的上装品类，均采用交领右衽的传统领式。二者以**单层为衫、夹层/有衬里为袄**为核心区分标准，衣长多覆盖腰臀区域，搭配马面裙等下装构成明代典型的袄裙/衫裙穿搭体系，上至宫廷命妇、下至平民女性均普遍穿着，是明代服饰文化中最具代表性的常服形制之一。


## 二、历史演变

1. **明初至明中期（洪武—弘治）**：交领右衽是女式上衣的主流领型，短衫、短袄为日常标配。出土文物显示，此阶段衫袄衣长约60厘米，仅掩裙腰，袖型为""鱼肚袖""（小琵琶袖），袖口收窄，整体形制简约利落。代表性实物有江西宁靖王夫人吴氏墓（弘治十五年，1502年）出土的交领短衫与夹袄。

2. **明中期（正德—嘉靖）**：衣衫尺寸逐渐放大，衣长延伸至膝下，袖宽显著增加，交领形制依然占据主流，但立领对襟款式开始出现并逐步流行。嘉靖年间女衣仍以交领衫、袄为主，身长普遍达到75~90厘米。

3. **明晚期（万历—崇祯）**：立领款式大范围普及，交领短衫袄逐渐退居内搭或下层民众常服，外穿更流行立领长袄与披风，但交领形制作为传统内衣和基础款始终沿用至明末。


## 三、形制结构

### （一）核心特征：交领右衽

- 衣领左右交叉，左襟压右襟，于右腋下系带固定，形成胸前三角形领口，是汉族服饰""右衽""传统的典型体现。

- 领口常配可拆卸护领（领缘），便于换洗，同时保护主衣领口免受磨损。


### （二）交领短衫

- **衣身**：通裁制，两侧开衩，衣长及腰至胯部，典型出土实物衣长约55~65厘米。

- **袖型**：主流为琵琶袖（鱼肚袖），袖根宽、袖口收窄，袖宽约30~35厘米，袖口宽11~17厘米；亦有窄袖、短袖款式作为内衣使用。

- **系带**：外襟设2~3根系带，与右腋下系带对应系合；里襟领端设1根系带，与左腋下内侧系带固定。

- **面料**：单层无衬里，多采用纱、罗、绢、棉布等轻薄面料，适合春夏穿着。


### （三）交领袄

- **衣身**：通裁开衩，结构与短衫相近，但因有衬里/填棉而更挺括；短袄衣长与短衫接近，长袄可过膝。

- **袖型**：以琵琶袖、大袖为主，袖型比同期衫子更饱满，冬季款式袖口收紧以利保暖。

