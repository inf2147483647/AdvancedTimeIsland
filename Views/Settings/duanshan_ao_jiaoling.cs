## 四、功能与适用场景

1. **日常起居**：交领短衫是明代女性最基础的日常便服，居家、劳作、出行皆可穿着，行动便捷。

2. **礼仪穿搭**：搭配马面裙、比甲、披风等，可构成从日常到次正式的完整穿搭体系，命妇阶层也将其作为常服内搭。

3. **保暖分层**：袄作为秋冬保暖层，可单穿或叠穿于衫外，配合披袄、披风应对严寒；衫则作为贴身层四季通用。

